-- Realtime Configuration
-- By default, Realtime is disabled on tables for security.
-- We must enable it for the tables we want to stream to the dashboard.
alter publication supabase_realtime add table public.logs;
alter publication supabase_realtime add table public.attendance;
alter publication supabase_realtime add table public.employees;

-- Row Level Security (RLS)
-- We use Service Role Key in the backend so it will bypass these by default,
-- but if we connect via frontend anon key, we'd need policies.
-- Let's turn RLS on just in case, but allow service_role everything.
ALTER TABLE public.employees ENABLE ROW LEVEL SECURITY;
ALTER TABLE public.attendance ENABLE ROW LEVEL SECURITY;
ALTER TABLE public.logs ENABLE ROW LEVEL SECURITY;
ALTER TABLE public.settings ENABLE ROW LEVEL SECURITY;
ALTER TABLE public.office_geofence ENABLE ROW LEVEL SECURITY;

CREATE POLICY "Service Role Full Access" ON public.employees FOR ALL USING (true) WITH CHECK (true);
CREATE POLICY "Service Role Full Access" ON public.attendance FOR ALL USING (true) WITH CHECK (true);
CREATE POLICY "Service Role Full Access" ON public.logs FOR ALL USING (true) WITH CHECK (true);
CREATE POLICY "Service Role Full Access" ON public.settings FOR ALL USING (true) WITH CHECK (true);
CREATE POLICY "Service Role Full Access" ON public.office_geofence FOR ALL USING (true) WITH CHECK (true);
